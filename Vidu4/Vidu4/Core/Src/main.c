/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_TIM1_Init();
  MX_TIM4_Init();
  /* USER CODE BEGIN 2 */
  
  /* --- SYSTEM INITIALIZATION (Adapted from Teacher's Code) --- */
  
  /* 1. Initialize Drivers (PWM, Encoder, DWT Delay) */
  /* This replaces the old init_main() function */
  Driver_Init_All(); 
  
  /* 2. Set Initial PWM State */
  PWM0_Set_Freq(1000); // Set PWM Frequency to 1kHz
  PWM0_Set_Duty(0);    // Motor Stop initially
  
  /* 3. Initialize PID Model */
  pid_initialize();

  /* Note: SysTick_Config is not needed here. 
     We use the default HAL SysTick (1ms) and handle the 5ms divider 
     inside SysTick_Handler (stm32f4xx_it.c) */

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
    
    /* --- CONTROL LOOP --- */
    /* tick_flag is set to 1 every 5ms by SysTick_Handler */
    if (tick_flag == 1)
    {
        tick_flag = 0; // Clear flag
        
        /* A. Read Sensor (Encoder) */
        /* Convert raw pulses to degrees: (Pos / 4000) * 360 */
        /* Note: In2 is defined in pid.h/pid.c */
        In2 = (double)ENC0_GetPos() / 4000.0 * 360.0;
        
        /* B. Run Control Algorithm (PID) */
        rt_OneStep();
        
        /* C. Process Output */
        /* Scale PID output for PWM duty cycle */
        /* Teacher's logic: Out1 * 1000 / 12 */
        Out1 = Out1 * 1000.0 / 12.0;
        
        /* D. Actuate Motor */
        udk = (int16_t)Out1;
        PWM0_Set_Duty(udk);
        
        /* E. Safety Break (Teacher's logic) */
        /* Stop loop after 4000 ticks (approx 20 seconds) */
        if (tick_count > 4000) {
            break; 
        }
    }
  }
  
  /* Stop Motor if loop exits */
  PWM0_Set_Duty(0);
  
  /* Prevent main() from returning */
  while(1);
  
  /* Terminate model (Unreachable in this context but good practice) */
  pid_terminate();
  
  /* USER CODE END 3 */
}

