/* * rt_OneStep: Executed every 5ms
 * This function steps the PID model generated by Matlab/Simulink.
 * Logic copied from main_f407.c
 */
void rt_OneStep(void)
{
  /* Check for overrun (previous step not finished) */
  if (OverrunFlag++) {
    rtmSetErrorStatus(pid_M, "Overrun");
    return;
  }

  /* Step the PID model */
  pid_step();

  /* Indicate task complete */
  OverrunFlag--;
}

